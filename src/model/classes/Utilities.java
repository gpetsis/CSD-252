package model.classes;

/**
 * Utilities class that holds final variables.
 *
 * @author Ioannis Petsis csd4993
 */
public class Utilities {

    /**
     * The width of the window.
     */
    private static final int WINWIDTH = 1000;

    /**
     * The height of the window,
     */
    private static final int WINHEIGHT = 880;

    /**
     * Window width getter.
     *
     * @return the width of the window
     */
    public static int getWIDTH(){ return WINWIDTH; }

    /**
     * Window height getter.
     *
     * @return the height of the window
     */
    public static  int getHEIGHT(){ return WINHEIGHT; }
}